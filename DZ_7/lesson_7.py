def cone_square_and_volume():
    radius = int(input('Введите радиус:'))
    height = int(input('Введите высоту:'))
    V = print('Объем цилиндра равен:',(3.14 * (radius ** 2)) * height)
    S = print('Площадь боковой поверхности цилиндра равна:',(2 * 3.14 * radius * height))
    return (V,S)


