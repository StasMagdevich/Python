# и его периметр. Функция должна вернуть два значения.

def triangle_square_and_perimeter():
    a = int(input('Введите длину первого катета:'))
    b = int(input('Введите длину второго катета:'))

    P = print('Периметр равен:', a + b + math.sqrt(a * a + b * b))
    S = print('Площадь равна:', a * b // 2)
    return (P,S)


